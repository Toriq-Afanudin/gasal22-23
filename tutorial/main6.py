# perulangan (loop)
# menggunakan list
angka2 = [0, 3, 2, 5, 10]
for i in angka2:
    print("i sekarang --> ", i)
print("akhir dari program\n")

# menggunakan range
for i in range(5):
    print("i sekarang --> ", i)
print("akhir dari program\n")

for i in range(1, 5):
    print("i sekarang --> ", i)
print("akhir dari program\n")

# menggunakan string
huruf = "saya suka makan"
for i in huruf:
    print("i sekarang --> ", i)
print("akhir dari program\n")
